        <h1 class="yellow-text">Statuten der dispuut DIABOLO</h1>
        <h6>Aldus aangenomen op de 3e dispuutsvergadering der dispuut DIABOLO, gehouden op 29 januari 2000.</h6>
        
        <h4>Artikel 1</h4>
        <hr> 
        <p>Er bestaat in Nederland een dispuut genaamd: DIABOLO, deze is opgericht op 20 april 1998, gevestigd te Eindhoven en wordt verder aangeduid als DIABOLO.</p>
        
        <h4>Artikel 2</h4>
        <hr>
        <p>DIABOLO stelt zich ten doel: a) het trachten op te lossen van de vergelijking y = ax; b) het zoeken naar de ultieme dispuutsdrank; c) iets toe te voegen aan de EINDHOVENSE STUDENTEN VERENIGING "DEMOS", verder aangeduid als Demos, als beschreven onder artikel 9.</p>
        
        <h4>Artikel 3</h4>
        <hr>
        <p>a) DIABOLO is een Heerendispuut. b) DIABOLO kent aspirantleden, leden, reünisten en devoten. Aspirant- en gewoon lidmaatschap staan enkel open voor leden van Demos. c) DIABOLO heeft minimaal 4 leden. Deze komen uit minimaal twee verschillende jaargangen van Demos. d) Bij het beëindigen van het lidmaatschap van Demos vervalt het recht op het lidmaatschap van DIABOLO. DIABOLO kan besluiten haar oud-leden het reünistenlidmaatschap toe te kennen. e) In geval van calamiteiten kan DIABOLO besluiten het lidmaatschap van een lid in te trekken.</p>
        
        <h4>Artikel 4</h4>
        <hr>
        <p>a) Aan het hoofd van DIABOLO staat het Anarchaat, dat de dagelijkse leiding heeft over DIABOLO en bestaat uit Ultimo Lazarus, Lazarus Secretus en Charon. Het Anarchaat wordt jaarlijks door en uit de leden van DIABOLO op democratische wijze verkozen. b) Besluitvorming geschied in en door de dispuutsvergadering, welke minimaal eens per jaar plaatsvindt. Alleen leden hebben stemrecht in de dispuutsvergadering; het stemrecht mag niet bij volmacht worden uitgeoefend.</p>
        
        <h4>Artikel 5</h4>
        <hr>
        <p>Een dispuutsvergadering wordt belegd indien het Anarchaat dit wenselijk acht of indien de statuten of reglementen dit voorschrijven. Iedere dispuutsvergadering dient voor het aanvangsttijdstip bekend gemaakt worden.</p>
        
        <h4>Artikel 6</h4>
        <hr>
        <p>DIABOLO onderscheidt zich van Demos door: a) dispuutskleding met minimaal het dispuutslogo en uitgevoerd in één of meerdere van de dispuutskleuren, te weten zwart, rood en geel; b) een vaste verplichte borrelavond in de sociëteit van Demos die periodiek vastgesteld wordt, met het voor de aanwezige leden verplicht, indien mogelijk en gewenst, dragen van een dispuutskledingstuk, gedragen op de/het voor dat kledingstuk bedoelde lichaamsde(e)l(en). Afwezigheid of het niet zichtbaar dragen van een dispuutskledingstuk op de borrelavond zonder geldig excuus, als beoordeeld door de overige aanwezige dispuutsleden, wordt bestraft met het geven van het equivalent van één klein krat, inhoud twaalf glazen flesjes gevuld met omstreeks dertig centiliter, van het bij voorkeur gedronken bier aan het dispuut door het nalatige lid.</p>
        
        <h4>Artikel 7</h4>
        <hr>
        <p>DIABOLO stelt zich ter nakoming dat: a) de statuten en reglementen van Demos nageleefd worden en het dispuut zich Demoswaardig gedraagt; b) er minimaal één maal per trimester een stukje in het verenigingsorgaan van Demos verschijnt voor en door DIABOLO; c) er minimaal één maal per jaar een dispuutsweekend georganiseerd wordt ter bezinning, ontspanning en vermaak; d) er minimaal één maal per jaar een activiteit voor Demos georganiseerd wordt; e) er jaarlijks een stunt uitgehaald wordt; f) zijn dies, als bepaald in artikel 1, jaarlijks gevierd wordt binnen Demos.</p>
        
        <h4>Artikel 8</h4>
        <hr>
        <p>In het Huishoudelijk Reglement wordt datgene geregeld waarin de statuten niet voorzien. Het Huishoudelijk Reglement mag geen bepaling bevatten die in strijd is met de statuten.</p>
        
        <h4>Artikel 9</h4>
        <hr>
        <p>In die gevallen waarin noch de statuten noch het Huishoudelijk Reglement voorzien beslist de dispuutsvergadering.</p>
        
        <h4>Artikel 10</h4>
        <hr>
        <p>Leden van DIABOLO zijn niet verplicht zich aan het Huishoudelijke Reglement der dispuut DIABOLO te houden. Zij hebben de vrijheid dit op eigen wijze te interpreteren.</p>
        
        <h4>Artikel 11</h4>
        <hr>
        <p>Een voorstel tot wijziging van de statuten kan door ieder lid voorgesteld worden. Een voorstel geldt als aangenomen, indien het op een dispuutsvergadering, waar tenminste drie/vierde van het aantal leden aanwezig is, wordt aangenomen met tenminste tweederde meerderheid van stemmen.</p>
        
        <h4>Artikel 12</h4>
        <hr>
        <p>Het dispuut kan alleen ontbonden worden, wanneer op een dispuutsvergadering ieder lid aanwezig is en voor ontbinding stemt, of indien gedurende een termijn langer dan zes aaneengesloten jaren niet voldaan wordt aan artikel 3 lid c. De bezittingen van het dispuut komen dan ten bate van een door de dispuutsvergadering vast te stellen doel. De liquidatie geschiedt door het Anarchaat.</p>
        
        <h1 class="yellow-text">Huishoudelijk Reglement</h1>
        <h6>Aldus aangenomen op de 3e dispuutsvergadering der dispuut DIABOLO, gehouden op 29 januari 2000.</h6>
        
        <h4>Hoofdstuk 1 Het lidmaatschap</h4>
        
        <h5>Artikel 1.01</h5>
        <hr>
        <p>DIABOLO kent: a) Aspirantleden; b) Leden; c) Reünisten;</p>
        
        <h5>Artikel 1.02</h5>
        <hr>
        <p>a) Leden zijn zie die: a) DIABOLO hebben opgericht en recht hebben op het lidmaatschap volgens de statuten artikel 3; b) door DIABOLO als lid toegelaten worden en als zodanig door het Anarchaat zijn geïnaugureerd; b) Leden hebben hebben alle rechten die hen te binnen schieten mits die niet in strijd zijn met de functies van het Anarchaat of de statuten der dispuut DIABOLO; c) Leden hebben de plicht zich te houden aan de statuten der dispuut DIABOLO;</p>
        
        <h5>Artikel 1.03</h5>
        <hr>
        <p>a) Aspirantleden worden geselecteerd door de leden van DIABOLO en treden vrijwillig toe als aspirantlid; b) Rechten der aspirantleden: 1. eten wat de pot schaft; 2. afwassen; c) Plichten der aspirantleden: 1. eten wat de pot schaft; 2. afwassen; d) Aspirantleden van DIABOLO kunnen lid worden als: 1. minstens driekwart van het dispuut instemt met het toekennen van het lidmaatschap; 2. het aspirantlid zorgvuldig door de leden ingewijd wordt op een door de leden te bepalen, niet mens-onterende of letsel-toebrengende wijze, voor een door de leden te bepalen duur;</p>
        
        <h5>Artikel 1.04</h5>
        <hr>
        <p>a) Reünisten zijn zij die: 1. geen recht meer hebben op het DIABOLO lidmaatschap volgens de statuten artikel 2; 2. door het Anarchaat hun lidmaatschap ontnomen is en het reünistenlidmaatschap is toegekend; 3. door DIABOLO uitvoerig als lid zijn uitgewijd op eervolle en respectabele wijze voor een door de leden te bepalen duur; b) Rechten der reünisten: 1. reünisten mogen de dispuutsvergaderingen bijwonen; 2. reünisten hebben geen stemrecht; 3. verder hebben reünisten alle rechten die hen te binnen schieten;</p>
        
        <h4>Hoofdstuk 2 Het Anarchaat</h4>
        
        <h5>Artikel 2.01</h5>
        <hr>
        <p>Het Anarchaat: a) staat aan het hoofd van het dispuut; b) bestaat uit een praeses, een secretaris en een penningmeester; c) wordt jaarlijks verkozen uit de leden der DIABOLO; d) roept de dispuutsvergaderingen uit en zorgt dat de tijd en plaats van de vergaderingen tijdig bij de leden bekend zijn;</p>
        
        <h5>Artikel 2.02</h5>
        <hr>
        <p>De praeses: a) leidt het Anarchaat en de vergaderingen; b) stelt de agenda op van de vergaderingen; c) is belast met het coördineren van de afwas bij dispuutsetentjes;</p>
        
        <h5>Artikel 2.03</h5>
        <hr>
        <p>De secretaris is belast met: a) het bijhouden van het archief; b) het (doen) notuleren en uitwerken van de notulen van dispuutsvergaderingen; c) het verzorgen van briefwisselingen voor zover deze niet op financiën betrekking heeft;</p>
        
        <h5>Artikel 2.04</h5>
        <hr>
        <p>De penningmeester is belast met: a) het beheer der financiën der DIABOLO; b) het voeren van alle briefwisselingen inzake de financiën van DIABOLO</p>
        
        <h4>Hoofdstuk 3 Activiteiten</h4>
        
        <h5>Artikel 3.01</h5>
        <hr>
        <p>DIABOLO zal minstens een maal per jaar een dispuutsweekend organiseren voor haar leden om te bezinnen over haar interne activiteiten, haar externe activiteiten binnen Demos, haar onderlinge band en haar toekomst, om te ontspannen en gruwelijk de beest uit te hangen;</p>
        
        <h5>Artikel 3.02</h5>
        <hr>
        <p>DIABOLO zal minstens een maal per jaar een activiteit voor Demos organiseren;</p>
        
        <h5>Artikel 3.03</h5>
        <hr>
        <p>DIABOLO zal trachten jaarlijks minimaal een stunt te organiseren ter stomme verbazing, ergenis en/of vermaak van bij voorkeur Demos, of andere verenigingen of instanties;</p>
        
        <h5>Artikel 3.04</h5>
        <hr>
        <p>DIABOLO zal ieder jaar op en/of rond de dag van oprichting haar dies uitgebreid vieren, zowel intern als binnen Demos;</p>
        
        <h5>Artikel 3.05</h5>
        <hr>
        <p>a) DIABOLO zal trachten minimaal een maal per maand een dispuutsetentje te organiseren; b) DIABOLO zal zich bij voorkeur uitnodigen voor het eten bij eerstejaars jaarclubs. In dat geval vervalt artikel 2.02 punt 3 van dit H.R.; c) Leden van DIABOLO dienen het volgende bij etentjes in acht te nemen; 1. eert de tong, het gehemelte en de maag van uw dispuutsgenoten; 2. gij zult niet in de soep spugen; 3. gij zult uw dispuutsgenoten voor slappe koffie behoeden;</p>
                
        
        
